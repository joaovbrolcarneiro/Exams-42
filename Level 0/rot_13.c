#include <unistd.h>

void rot_13(char *str)
{
    while (*str != '\0')
    {
        if ((*str >= 'a' && *str <= 'z') || (*str >= 'A' && *str <= 'Z'))
        {
            if ((*str >= 'a' && *str <= 'm') || (*str >= 'A' && *str <= 'M'))
                *str = *str + 13;
            else
                *str = *str - 13;

            write(1, str, 1);
        }
        else
        {
            write(1, str, 1);
        }
        str++;
    }
}
