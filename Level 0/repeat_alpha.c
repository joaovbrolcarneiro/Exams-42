void repeat_alpha(char *str)
{
	int i = 0;

	while (str[i] != '\0')
	{
		char ch = str[i];
		int n = 0;

	if ((ch >= 'a' && ch <= 'z') || (ch >= 'A' && ch <= 'Z'))
	{
		if (ch >= 'a' && ch <= 'z')
		{
			n = ch - 'a' + 1;
		}
		else
		{
			n = ch - 'A' + 1;
		}

		while (n > 0)
		{
			write (1, &ch, 1);
			n--;
		}
	}
	else
	{
		write (1, &ch, 1);
	}
	i++;
	}
}
